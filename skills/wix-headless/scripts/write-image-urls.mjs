#!/usr/bin/env node
// Write .wix/image-urls.md from the Image Phase 1 agent's structured return.
//
// The image agent returns `data.imageUrls = { <slot>: <url>, ... }` in its JSON
// block. The orchestrator passes that object on stdin to this script, which
// writes the file in the exact `## key` + `- url: <url>` format that
// scripts/patch-decorative-slots.mjs parses.
//
// Centralizing this responsibility on the orchestrator (rather than the agent)
// avoids two failure modes observed in real runs:
//   1. Agents lacking Write/Bash permission for the project directory return
//      `partial` after successfully uploading the images, with URLs in
//      `data.imageUrls` but no file on disk. The downstream patcher then sees
//      no image-urls.md and skips. Promoting orchestrator-writes-file to the
//      primary path eliminates this entirely.
//   2. Format drift between the agent's writer and the patcher's parser — the
//      patcher expects `## key\n- url: <url>` blocks. An agent that drifts
//      (e.g. emits a markdown table) silently produces an empty url map.
//
// Usage:
//   echo '{"imageUrls": {"hero": "https://…", "about": "https://…"}}' \
//     | node write-image-urls.mjs <project-dir>
//
// stdin can be the full `data` block returned by the agent (extra keys
// ignored) or just `{imageUrls: …}`. Top-level `imageUrls` is also accepted
// for orchestrator convenience:
//   echo '{"hero": "https://…"}' | node write-image-urls.mjs <project-dir> --bare
//
// Behavior:
//   - Refuses to overwrite an existing .wix/image-urls.md (exit 2) unless
//     --force is passed. Setup should call this exactly once per build.
//   - Creates .wix/ if it doesn't exist.
//   - Validates each URL is `https://…` — non-https values fail the build
//     loudly rather than silently shipping a broken slot.
//   - Outputs a JSON summary to stdout: `{status, path, slotCount}`.
//
// Exit codes:
//   0 — wrote the file (or --force-overwrote it)
//   2 — argument validation, malformed stdin, or file exists without --force

import { writeFileSync, existsSync, mkdirSync, readFileSync } from "node:fs";
import { join } from "node:path";

const args = process.argv.slice(2);
const projectDir = args.find((a) => !a.startsWith("--"));
const force = args.includes("--force");
const bare = args.includes("--bare");

if (!projectDir) {
  console.error("usage: cat data.json | write-image-urls.mjs <project-dir> [--force] [--bare]");
  process.exit(2);
}

let stdin;
try {
  stdin = JSON.parse(readFileSync(0, "utf8"));
} catch (e) {
  console.error(`write-image-urls: stdin is not valid JSON (${e.message})`);
  process.exit(2);
}

// Accept either `{imageUrls: {...}}` (agent return data block) or
// `{<slot>: <url>, ...}` with --bare (orchestrator convenience).
const map = bare ? stdin : stdin.imageUrls;
if (!map || typeof map !== "object" || Array.isArray(map)) {
  console.error("write-image-urls: input must contain `imageUrls` (object) — or pass --bare with the map as the root");
  process.exit(2);
}

const entries = Object.entries(map);
if (entries.length === 0) {
  console.error("write-image-urls: imageUrls is empty — nothing to write");
  process.exit(2);
}

for (const [slot, url] of entries) {
  if (typeof url !== "string" || !/^https:\/\//.test(url)) {
    console.error(`write-image-urls: slot "${slot}" has non-https url: ${JSON.stringify(url)}`);
    process.exit(2);
  }
}

const wixDir = join(projectDir, ".wix");
const filePath = join(wixDir, "image-urls.md");

if (existsSync(filePath) && !force) {
  console.error(`write-image-urls: ${filePath} already exists — refusing to overwrite. Pass --force to overwrite (e.g. after a credit-recovery retry).`);
  process.exit(2);
}

mkdirSync(wixDir, { recursive: true });

const lines = ["# Image URLs", "", "Generated by Image Phase 1. Consumed by scripts/patch-decorative-slots.mjs.", ""];
for (const [slot, url] of entries) {
  lines.push(`## ${slot}`);
  lines.push(`- url: ${url}`);
  lines.push("");
}

writeFileSync(filePath, lines.join("\n"));

console.log(JSON.stringify({ status: "ok", path: filePath, slotCount: entries.length, slots: entries.map(([k]) => k) }, null, 2));
